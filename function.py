
def add(num_one, numb_two):
    """
    Add two numbers and return the result.

    Parameters:
    a (int or float): The first number to add.
    b (int or float): The second number to add.

    Returns:
    int or float: The sum of the two numbers.
    """
    return num_one + numb_two


def subtract(num_one, numb_two):
    """
    Substract two numbers and return the result.

    Parameters:
    a (int or float): The first number to substract.
    b (int or float): The second number to substract.

    Returns:
    int or float: The substraction of the two numbers.
    """
    return num_one - numb_two
